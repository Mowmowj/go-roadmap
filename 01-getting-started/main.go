package main

import "fmt"

// ============================================================================
// 📚 概念：什么是 Go 语言？
// ============================================================================
// Go（又称 Golang）是 Google 在 2009 年发布的开源编程语言。
// 由 Robert Griesemer、Rob Pike 和 Ken Thompson 设计。
//
// Go 的核心设计理念：
// - 简洁：语法极简，关键字只有 25 个
// - 高效：编译速度快，运行性能接近 C
// - 并发：内置 goroutine 和 channel，原生支持并发
// - 安全：强类型、垃圾回收、无指针运算
// - 工程化：内置格式化、测试、文档工具
//
// Go 适合的场景：
// - 后端服务 / 微服务
// - 云原生应用（Docker、Kubernetes 都是 Go 写的）
// - 命令行工具
// - 网络编程
// - DevOps 工具
// ============================================================================

func main() {
	// ========================================================================
	// 1. Hello World - 每个 Go 程序从 main 包的 main 函数开始
	// ========================================================================
	fmt.Println("Hello, Go! 🚀")
	fmt.Println("欢迎来到 Go 语言学习之旅！")

	// ========================================================================
	// 2. fmt 包基础输出
	// ========================================================================
	// Println - 打印并换行
	fmt.Println("--- fmt 包基础输出 ---")
	fmt.Println("Println: 自动换行")

	// Printf - 格式化输出（不自动换行）
	name := "Go"
	version := 1.19
	fmt.Printf("Printf: 语言=%s, 版本=%.2f\n", name, version)

	// Sprintf - 格式化为字符串（不输出）
	result := fmt.Sprintf("Sprintf: %s %.0f", name, version)
	fmt.Println(result)

	// 常用格式化动词
	fmt.Println("\n--- 格式化动词 ---")
	fmt.Printf("%%v  通用格式:  %v\n", 42)
	fmt.Printf("%%d  十进制:    %d\n", 42)
	fmt.Printf("%%s  字符串:    %s\n", "hello")
	fmt.Printf("%%f  浮点数:    %f\n", 3.14)
	fmt.Printf("%%t  布尔值:    %t\n", true)
	fmt.Printf("%%T  类型:      %T\n", 42)
	fmt.Printf("%%p  指针:      %p\n", &name)
	fmt.Printf("%%b  二进制:    %b\n", 42)
	fmt.Printf("%%x  十六进制:  %x\n", 255)
	fmt.Printf("%%o  八进制:    %o\n", 42)

	// ========================================================================
	// 3. Go 程序基本结构说明
	// ========================================================================
	fmt.Println("\n--- Go 程序结构 ---")
	fmt.Println(`每个 Go 程序包含：
1. package 声明    - 每个文件必须属于一个包
2. import 导入     - 引入需要的包
3. func main()    - 程序入口点（仅 main 包有）

Go 文件结构：
┌─────────────────────┐
│ package main        │  ← 包声明
│                     │
│ import "fmt"        │  ← 导入
│                     │
│ func main() {       │  ← 入口函数
│     fmt.Println()   │
│ }                   │
└─────────────────────┘`)

	// ========================================================================
	// 4. Go 命令速查
	// ========================================================================
	fmt.Println("--- Go 命令速查 ---")
	fmt.Println(`常用 go 命令：
  go run main.go       编译并运行程序
  go build              编译生成可执行文件
  go test               运行测试
  go fmt                格式化代码
  go vet                静态分析检查
  go mod init <name>    初始化模块
  go mod tidy           整理依赖
  go get <pkg>          下载依赖包
  go doc <pkg>          查看文档
  go version            查看 Go 版本
  go env                查看环境变量`)
}

